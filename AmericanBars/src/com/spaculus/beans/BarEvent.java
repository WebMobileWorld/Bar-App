package com.spaculus.beans;

public class BarEvent {

	// Private Variables
	private String id = "";
	private String title = "";
	private String desc = "";
	private String start_date = "";
	private String event_image = "";
	private String event_lat = "";
	private String event_lng = "";
	private String address = "";
	private String city = "";
	private String state = "";
	private String zipcode = "";

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getStart_date() {
		return start_date;
	}

	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}

	public String getEvent_image() {
		return event_image;
	}

	public void setEvent_image(String event_image) {
		this.event_image = event_image;
	}

	public String getEvent_lat() {
		return event_lat;
	}

	public void setEvent_lat(String event_lat) {
		this.event_lat = event_lat;
	}

	public String getEvent_lng() {
		return event_lng;
	}

	public void setEvent_lng(String event_lng) {
		this.event_lng = event_lng;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getZipcode() {
		return zipcode;
	}

	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
}
