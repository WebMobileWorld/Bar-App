package com.spaculus.beans;

public class BarEventDateTime {

	private String sss_event_time_id = "";
	private String eventdate = "";
	private String eventstarttime = "";
	private String eventendtime = "";
	private String event_id = "";

	public String getSss_event_time_id() {
		return sss_event_time_id;
	}

	public void setSss_event_time_id(String sss_event_time_id) {
		this.sss_event_time_id = sss_event_time_id;
	}

	public String getEventdate() {
		return eventdate;
	}

	public void setEventdate(String eventdate) {
		this.eventdate = eventdate;
	}

	public String getEventstarttime() {
		return eventstarttime;
	}

	public void setEventstarttime(String eventstarttime) {
		this.eventstarttime = eventstarttime;
	}

	public String getEventendtime() {
		return eventendtime;
	}

	public void setEventendtime(String eventendtime) {
		this.eventendtime = eventendtime;
	}

	public String getEvent_id() {
		return event_id;
	}

	public void setEvent_id(String event_id) {
		this.event_id = event_id;
	}
}
